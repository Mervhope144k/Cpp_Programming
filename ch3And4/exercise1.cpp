//Program to calculate the square root of 3 numbers
#include <iostream>
#include <cmath>

using namespace std;

int main(){
	cout << "Number" << "		" << "Square Root" << endl;
	cout << " 4" << "		" << sqrt(4) << endl;
	cout << "12.25" << "		" << sqrt(12.25) << endl;
	cout << " 0.0121" << "		" << sqrt(0.0121) << endl;

	return 0;
}
